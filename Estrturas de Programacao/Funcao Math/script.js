//funcoes matematicas
let numero1 = prompt("Digite um numero");
let numero2 = prompt("Digite outro numero");
console.log(Math.max(numero1, numero2)); //retorna o maior numero
console.log(Math.min(numero1, numero1)); //retorna o menor numero
console.log(Math.round(numero1)); //arredonda para o inteiro mais proximo
console.log(Math.floor(numero1)); //arredonda para o inteiro mais proximo
console.log(Math.ceil(numero1)); //arredonda para o inteiro mais proximo
console.log(Math.abs(numero1)); //retorna o valor absoluto
console.log(Math.sqrt(numero1)); //retorna a raiz quadrada
console.log(Math.pow(numero1, numero2)); //retorna o numero1 elevado a numero2
console.log(Math.PI); //retorna o valor de PI
console.log(Math.E); //retorna o valor de E
console.log(Math.sin(numero1)); //retorna o seno de numero1
console.log(Math.cos(numero1)); //retorna o cosseno de numero1
console.log(Math.tan(numero1)); //retorna a tangente de numero1
console.log(Math.log(numero1)); //retorna o logaritmo de numero1
console.log(Math.exp(numero1)); //retorna o exponencial de numero1
console.log(Math.random()); //retorna um numero aleatorio entre 0 e 1
console.log(Math.random() * numero1); //retorna um numero aleatorio entre 0 e numero1
console.log(Math.random() * (numero1 - numero2) + numero2); //retorna um numero aleatorio entre numero2 e numero1
console.log(Math.floor(Math.random() * (numero1 - numero2) + numero2)); //retorna um numero aleatorio entre numero2 e numero1
console.log(Math.floor(Math.random() * (numero1 - numero2) + numero2)); //retorna um numero aleatorio entre numero2 e numero1