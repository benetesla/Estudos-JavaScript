//funcoes matematicas
function soma(a, b) {
    return a + b;
}

function subtracao(a, b) {
    return a - b;
}

function multiplicacao(a, b) {
    return a * b;
}

function divisao(a, b) {
    return a / b;
}

function resto(a, b) {
    return a % b;
}
console.log(soma(5, 8));
console.log(subtracao(5, 8));
console.log(multiplicacao(5, 8));
console.log(divisao(5, 8));
console.log(resto(5, 8));
