//numeros 
let number1 = 5;
let number2 = 8;
let number3 = 10.5;
console.log(number1 + number2); //soma
console.log(number1 - number2); //subtracao
console.log(number1 * number2); //multiplicacao
console.log(number1 / number2); //divisao
console.log(number1 % number2); //resto da divisao
console.log(number1 ** number2); //exponenciacao
console.log(number1 + number2 + number3); //soma de varios numeros